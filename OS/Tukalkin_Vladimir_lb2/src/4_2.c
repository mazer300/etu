/*
 * Назначение программы:
 * Данная программа демонстрирует многонитевое функционирование в Linux.
 * Главный процесс создает несколько потоков (нитей) с использованием библиотеки POSIX Threads (pthread).
 * Каждый поток выполняет простую задачу, имитируя работу через цикл с задержкой.
 * В процессе работы осуществляется вывод информации о каждом потоке: его идентификаторе потока (TID) и идентификаторе процесса (PID).
 * Также демонстрируется использование команды ps для отображения состояния всех потоков процесса.
 * После завершения всех нитей главный процесс корректно завершает свою работу.
 *
 * Основные функции:
 * - pthread_create() — создание новых потоков;
 * - pthread_self() — получение идентификатора потока;
 * - pthread_join() — ожидание завершения потоков;
 * - system("ps -L -p PID") — вывод информации о процессах и их нитях.
 *
 * Результаты работы программы позволяют проанализировать идентификацию потоков,
 * их параллельное выполнение и особенности их управления в рамках одного процесса.
 */

#include <pthread.h>
#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>

#define NUM_THREADS 4  // Количество потоков

void* thread_function(void* arg) {
    long tid = (long)arg;
    printf("Поток %ld запущен (TID=%lu, PID=%d)\n", tid, pthread_self(), getpid());

    // Имитация работы потока
    for (int i = 0; i < 3; i++) {
        printf("Поток %ld выполняет итерацию %d\n", tid, i + 1);
        sleep(1);
    }

    printf("Поток %ld завершен\n", tid);
    pthread_exit(NULL);
}

int main() {
    pthread_t threads[NUM_THREADS];

    printf("Главный процесс (PID=%d) запускает потоки...\n", getpid());

    for (long i = 0; i < NUM_THREADS; i++) {
        if (pthread_create(&threads[i], NULL, thread_function, (void*)i) != 0) {
            perror("Ошибка создания потока");
            exit(1);
        }
    }

    // Печать списка потоков процесса
    printf("\nСостояние потоков процесса:\n");
    char cmd[128];
    snprintf(cmd, sizeof(cmd), "ps -L -p %d", getpid());
    system(cmd);
    printf("\n");

    // Ожидание завершения всех потоков
    for (int i = 0; i < NUM_THREADS; i++) {
        pthread_join(threads[i], NULL);
    }

    printf("Главный процесс завершен\n");
    return 0;
}
