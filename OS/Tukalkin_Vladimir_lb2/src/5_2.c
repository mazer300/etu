/*
 * Программа для демонстрации изменения приоритета процесса с помощью nice() и getpriority().
 *
 * Задание: 5.2. Ознакомиться с выполнением команды nice(1) и системного вызова getpriority(2).
 * Изменить приоритет процесса и зафиксировать реакцию системы.
 *
 * Описание:
 * - Получает и выводит старый приоритет процесса.
 * - Изменяет значение "niceness" с помощью функции nice().
 * - Получает и выводит новый приоритет процесса.
 * - Процесс выполняется длительно для наблюдения в таблице процессов.
 *
 * Цели:
 * - Показать изменение приоритета процесса на уровне пользователя.
 * - Позволить наблюдать поведение процесса с измененным приоритетом.
 *
 * Компиляция:
 * gcc 5_2.c -o 5_2
 *
 * Запуск:
 * ./5_2
 */

#include <stdio.h>
#include <unistd.h>
#include <sys/resource.h>

int main() {
    int old_prio = getpriority(PRIO_PROCESS, 0);
    printf("Старый приоритет: %d\n", old_prio);

    nice(10);  // Увеличим значение nice (понизим приоритет)

    int new_prio = getpriority(PRIO_PROCESS, 0);
    printf("Новый приоритет: %d\n", new_prio);

    while (1);  // Долгое выполнение для тестов
    return 0;
}
