/*
 * Назначение программы:
 * Данная программа демонстрирует наследование открытых файлов между родительским и дочерним процессами после вызова fork().
 * Родительский процесс открывает файл, затем создает дочерний процесс.
 * Оба процесса пытаются записать данные в один и тот же файл.
 *
 * Основные функции:
 * - fork() — создание дочернего процесса;
 * - open(), write(), close() — работа с файловым дескриптором;
 * - getpid() — получение PID процесса.
 *
 * Вывод программы позволяет убедиться, что файловый дескриптор наследуется дочерним процессом.
 */

#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>
#include <fcntl.h>
#include <sys/types.h>
#include <sys/wait.h>

int main() {
    int fd;
    pid_t pid;

    // Открываем файл для записи (если нет — создаем)
    fd = open("shared_file.txt", O_CREAT | O_WRONLY | O_APPEND, 0644);
    if (fd == -1) {
        perror("Ошибка открытия файла");
        exit(1);
    }

    printf("Файл открыт в процессе PID=%d, файловый дескриптор = %d\n", getpid(), fd);

    pid = fork();

    if (pid < 0) {
        perror("Ошибка fork");
        exit(1);
    } else if (pid == 0) {
        // Дочерний процесс
        printf("Дочерний процесс PID=%d записывает в файл...\n", getpid());
        dprintf(fd, "Дочерний процесс PID=%d записал строку\n", getpid());
        close(fd);
        exit(0);
    } else {
        // Родительский процесс
        printf("Родительский процесс PID=%d записывает в файл...\n", getpid());
        dprintf(fd, "Родительский процесс PID=%d записал строку\n", getpid());
        wait(NULL); // Ждем завершения потомка
        close(fd);
    }

    printf("Родительский процесс завершен\n");
    return 0;
}
